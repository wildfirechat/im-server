package com.xiaoleilu.loServer.pojos;


public class InputOutputUserBlockStatus {
    private String userId;
    private int status;

    public InputOutputUserBlockStatus(String userId, int status) {
        this.userId = userId;
        this.status = status;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
